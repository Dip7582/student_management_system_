    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        String rollno = jTextField1.getText();
        String physics = jTextField2.getText();
        String chemistry = jTextField3.getText();
        String maths = jTextField4.getText();
        String EM = jTextField5.getText();
        String DBMS = jTextField6.getText();
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/Student_management_system","root","Dipak@7582");
            Statement st = con.createStatement();
            ResultSet rs=st.executeQuery("select * from student where rollno = '"+rollno+"'");
            if(rs.next()){
                st.executeUpdate("insert into result (rollno,physics,chemistry,Maths,EM,DBMS)values ('"+rollno+"','"+physics+"','"+chemistry+"','"+maths+"','"+EM+"','"+DBMS+"')");
                JOptionPane.showMessageDialog(null,"Succesfully added the data");
                setVisible(false);
                new insertnewresult().setVisible(true);

            }
            else{
                JOptionPane.showMessageDialog(null,"this roll no does not exist");
            }

        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e.toString());
        }
    }//GEN-LAST:event_jButton6ActionPerformed

